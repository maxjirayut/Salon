package com.example.mallo.salon.Model;

public class StyleModel {

    String style_id;
    String style_name;
    String style_img;
    String style_meny;
    String style_detail;
    String salon_id;

    public String getStyle_id() {
        return style_id;
    }

    public void setStyle_id(String style_id) {
        this.style_id = style_id;
    }

    public String getStyle_name() {
        return style_name;
    }

    public void setStyle_name(String style_name) {
        this.style_name = style_name;
    }

    public String getStyle_img() {
        return style_img;
    }

    public void setStyle_img(String style_img) {
        this.style_img = style_img;
    }

    public String getStyle_meny() {
        return style_meny;
    }

    public void setStyle_meny(String style_meny) {
        this.style_meny = style_meny;
    }

    public String getStyle_detail() {
        return style_detail;
    }

    public void setStyle_detail(String style_detail) {
        this.style_detail = style_detail;
    }

    public String getSalon_id() {
        return salon_id;
    }

    public void setSalon_id(String salon_id) {
        this.salon_id = salon_id;
    }
}
